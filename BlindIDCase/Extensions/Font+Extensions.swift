import SwiftUI

extension Font {
    static func robotoRegular(size: CGFloat) -> Font {
        return .custom("Roboto-Regular", size: size)
    }

    static func robotoItalic(size: CGFloat) -> Font {
        return .custom("Roboto-Italic", size: size)
    }

    static func robotoLight(size: CGFloat) -> Font {
        return .custom("Roboto-Light", size: size)
    }

    static func robotoLightItalic(size: CGFloat) -> Font {
        return .custom("Roboto-LightItalic", size: size)
    }

    static func robotoMedium(size: CGFloat) -> Font {
        return .custom("Roboto-Medium", size: size)
    }

    static func robotoMediumItalic(size: CGFloat) -> Font {
        return .custom("Roboto-MediumItalic", size: size)
    }

    static func robotoBold(size: CGFloat) -> Font {
        return .custom("Roboto-Bold", size: size)
    }

    static func robotoBoldItalic(size: CGFloat) -> Font {
        return .custom("Roboto-BoldItalic", size: size)
    }

    static func robotoBlack(size: CGFloat) -> Font {
        return .custom("Roboto-Black", size: size)
    }

    static func robotoBlackItalic(size: CGFloat) -> Font {
        return .custom("Roboto-BlackItalic", size: size)
    }

    static func robotoCondensed(size: CGFloat) -> Font {
        return .custom("Roboto-Condensed", size: size)
    }
}
